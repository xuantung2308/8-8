                    <div class="atitle" style="text-align: left ;"><b>2. CÁCH TÍNH CHỈ SỐ KHỐI CƠ THỂ BMI?</b></div>
                    <p>
                    Chỉ số BMI được tính dựa trên chiều cao và cân nặng theo công thức:  <br>
                    BMI=(Cân nặng)/(Chiều cao x Chiều cao)<br>
                    Trong đó cân nặng của một người tính bằng kilôgam, chiều cao tính bằng mét. <br>
                    Ví dụ: Bạn cao 1,7m, nặng 65kg thì BMI = 65 ÷ (1,7x1,7) =  22,49 kg/m2.
                    </p>
                    <div class="atitle" style="text-align: left ;"><b>3. GIẢI THÍCH CHỈ SỐ KHỐI CƠ THỂ BMI</b></div>
                    <p>
                    Theo phân loại của Hiệp hội đái đường các nước châu Á (IDI & WPRO), chỉ số BMI của người châu Á từ 18,50 - 22,9 kg/m2 là cân nặng bình thường. BMI dưới 18,5 là nhẹ cân, từ 23 – 24,9 là thừa cân và từ 25 trở lên là béo phì. <br>
                    Còn chỉ số BMI theo phân loại quốc tế (WHO) thì dưới 18,5 là nhẹ cân, 18,5-24,9 là bình thường, 25-29,9 là thừa cân và từ 30 trở lên là béo phì. <br>
                    Chỉ số trên đánh giá chính xác trọng lượng cơ thể ở gần hết người lớn trên 19 tuổi. Song chỉ số này không áp dụng ở những người có thân hình cơ bắp (vd: vận động viên, tập thể hình), phụ nữ mang thai, người già và người bị mất khối lượng cơ.<br>
                    Ở trẻ em từ 5-19 tuổi sẽ giải thích chỉ số BMI phụ thuộc vào giới tính và độ tuổi cụ thể dựa trên bảng chuẩn của Tổ chức Y tế Thế giới (WHO). 
                    </p>
                    <div class="atitle" style="text-align: left ;"><b>4. MỐI TƯƠNG QUAN GIỮA BMI VÀ CHẤT BÉO TRONG CƠ THỂ</b></div>
                    <p>
                    BMI càng cao thì lượng chất béo dư thừa trong cơ thể càng nhiều. Nhưng ngay cả khi hai người có cùng chỉ số BMI thì mức độ chất béo trong cơ thể có thể khác nhau.<br> 
                    Có cùng một chỉ số BMI, nhưng:<br>
                    - Phụ nữ có nhiều chất béo trong cơ thể hơn nam giới.<br>
                    - Chủng tộc/dân tộc khác nhau sẽ có lượng chất béo trong cơ thể khác nhau.<br>
                    - Trung bình người lớn tuổi có nhiều chất béo trong cơ thể hơn người trẻ tuổi.<br>
                    - Vận động viên, người lao động chân tay có ít chất béo trong cơ thể hơn người không vận động.
                    </p>
