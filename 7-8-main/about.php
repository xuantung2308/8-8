                    Tình trạng tăng cân xảy ra do lượng calo dung nạp nhiều hơn lượng calo tiêu thụ. Nếu chỉ số BMI cao, bạn cần giảm cân, vì tình trạng thừa cân béo phì sẽ làm tăng nguy cơ mắc một số bệnh lý như:<br>
                    - Tăng huyết áp<br>
                    - Rối loạn lipid máu<br>
                    - Đái tháo đường<br>
                    - Bệnh tim mạch<br>
