                        Chỉ số khối cơ thể (BMI - Body mass index) là một phép tính dựa trên chiều cao và cân nặng,
                        giúp xác định xem một người có cân nặng chuẩn, nhẹ cân, thừa cân hay béo phì. BMI không đo 
                        trực tiếp lượng chất béo trong cơ thể, nhưng BMI có thể đánh giá tương đối về lượng chất béo 
